    //Validar los campos
    public boolean validar() {
        String id       = txtIdOferta.getText();
        String mat      = txtIdMatricula.getText();
        String desc     = txtDescripcionOferta.getText();
        String precio   = txtPrecioOferta.getText();

        if (id.isEmpty()    || id.equals("ID Oferta") ||
            mat.isEmpty()   || mat.equals("Matrícula") ||
            desc.isEmpty()  || desc.equals("Descripción Oferta") ||
            precio.isEmpty()|| precio.equals("Precio Oferta")) {
            JOptionPane.showMessageDialog(null, "Todos los campos son obligatorios");
            return false;
        }

        double precioOferta;
        try {
            Integer.parseInt(id);
            precioOferta = Double.parseDouble(precio);
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "ID Oferta debe ser entero y Precio debe ser numérico");
            return false;
        }

        // Requisito VII: precio no menor al 15% del precio gama
        if (precioGamaActual > 0) {
            double minimo = precioGamaActual * 0.15;
            if (precioOferta < minimo) {
                JOptionPane.showMessageDialog(null,
                    String.format("El Precio Oferta no puede ser menor al 15%% del Precio Gama.\n" +
                                  "Precio Gama: %.2f  →  Mínimo permitido: %.2f",
                                  precioGamaActual, minimo),
                    "Precio inválido", JOptionPane.WARNING_MESSAGE);
                return false;
            }
        }
        return true;
    }
