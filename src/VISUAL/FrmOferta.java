    private void buscarYMostrarVehiculo(String matricula) {
        try {
            Vehiculo v = new VehiculoDAO().buscarPorMatricula(matricula);
            if (v != null) {
                // busca el precio de la gama del vehículo
                Gama g = new GamaDAO().buscarPorId(v.idGama);
                double precioGama = (g != null) ? g.precio : 0.0;
                precioGamaActual = precioGama;

                lblInfoVehiculo.setText(
                    "Vehículo: " + v.marca + " " + v.modelo + "  |  " + v.matricula);
                lblPrecioGama.setText(
                    String.format("Precio Gama: %.2f  (mín. oferta: %.2f)",
                                  precioGama, precioGama * 0.15));
                lblInfoVehiculo.setForeground(new java.awt.Color(0, 128, 0));
                lblPrecioGama.setForeground(new java.awt.Color(0, 0, 180));
            } else {
                JOptionPane.showMessageDialog(null,
                    "La matrícula '" + matricula + "' no existe en el archivo de vehículos.",
                    "Matrícula no encontrada", JOptionPane.WARNING_MESSAGE);
                lblInfoVehiculo.setText("Vehículo: no encontrado");
                lblPrecioGama.setText("Precio Gama: -");
                lblInfoVehiculo.setForeground(java.awt.Color.RED);
                precioGamaActual = 0.0;
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
    
