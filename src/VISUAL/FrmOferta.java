    private void txtIdOfertaFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_txtIdOfertaFocusLost
          String idStr = txtIdOferta.getText().trim();
        if (idStr.isEmpty() || idStr.equals("ID Oferta")) return;
        try {
            int id = Integer.parseInt(idStr);
            Oferta encontrada = new OfertaDAO().buscarPorId(id);
            if (encontrada != null) {
                JOptionPane.showMessageDialog(null, "Modificando");
                txtIdMatricula.setText(encontrada.idMatricula);
                txtIdMatricula.setForeground(java.awt.Color.BLACK);
                txtDescripcionOferta.setText(encontrada.descripcionOferta);
                txtDescripcionOferta.setForeground(java.awt.Color.BLACK);
                txtPrecioOferta.setText(String.valueOf(encontrada.precioOferta));
                txtPrecioOferta.setForeground(java.awt.Color.BLACK);
                // también carga la info del vehículo asociado
                buscarYMostrarVehiculo(encontrada.idMatricula);
            } else {
                JOptionPane.showMessageDialog(null, "Creando");
                txtIdMatricula.setText("");
                txtDescripcionOferta.setText("");
                txtPrecioOferta.setText("");
                lblInfoVehiculo.setText("Vehículo: -");
                lblPrecioGama.setText("Precio Gama: -");
                precioGamaActual = 0.0;
                aplicarPlaceholders();
                txtIdOferta.setText(idStr);
                txtIdOferta.setForeground(java.awt.Color.BLACK);
            }
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(null, "ID Oferta debe ser un número entero");
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_txtIdOfertaFocusLost

